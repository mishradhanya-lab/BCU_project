/**
 * @brief   Wheel identifier index — used as array index throughout
 */
typedef enum
{
    WHEEL_FL = 0U,  /**< Front Left  */
    WHEEL_FR = 1U,  /**< Front Right */
    WHEEL_RL = 2U,  /**< Rear Left   */
    WHEEL_RR = 3U,  /**< Rear Right  */
    WHEEL_COUNT     /**< Must be last — used for array sizing */
} WheelId_t;

/**
 * @brief   ABS solenoid valve state for one wheel
 *          Transitions: NORMAL → HOLD → DUMP → BUILD → NORMAL
 */
typedef enum
{
    SOLENOID_NORMAL = 0U,   /**< Both valves de-energised: pressure follows pedal */
    SOLENOID_HOLD   = 1U,   /**< Inlet closed, outlet closed: pressure held       */
    SOLENOID_DUMP   = 2U,   /**< Inlet closed, outlet open:  pressure reduced     */
    SOLENOID_BUILD  = 3U,   /**< Inlet open,   outlet closed: pressure increases  */
} SolenoidState_t;

/**
 * @brief   ABS controller state machine for one wheel
 */
typedef enum
{
    ABS_STATE_INACTIVE = 0U,   /**< Normal braking, no slip detected               */
    ABS_STATE_HOLD,            /**< Slip detected: holding pressure                */
    ABS_STATE_DUMP,            /**< Pressure being released to recover wheel speed */
    ABS_STATE_BUILD,           /**< Pressure being reapplied (ramp up)             */
} ABSState_t;

/**
 * @brief   Per-wheel runtime data
 *          Placed in CCM SRAM for zero-wait-state access in time-critical ISR
 *          __attribute__((section(".ccmram"))) — set in linker script
 */
typedef struct
{
    /* Wheel speed sensor data */
    volatile uint32_t   pulse_count;       /**< Raw encoder pulse count (updated in TIM capture ISR) */
    volatile uint32_t   last_capture_tick; /**< SysTick ms at last pulse — for timeout detection     */
    float               speed_mps;         /**< Calculated wheel speed (m/s)                         */
    float               speed_prev_mps;    /**< Previous cycle speed — for deceleration calc         */
    float               slip_ratio;        /**< (V_ref - V_wheel) / V_ref                            */
    float               decel_g;           /**< Wheel deceleration in g                               */

    /* ABS state machine */
    ABSState_t          abs_state;
    SolenoidState_t     solenoid_state;
    uint32_t            abs_cycle_count;   /**< Debug: number of ABS cycles this brake event         */

    /* Diagnostics */
    bool                sensor_fault;      /**< True if WSS pulse timeout or implausible speed       */
    uint32_t            fault_code;        /**< DTC code — stored in NVM on ignition off             */
} __attribute__((aligned(4))) WheelData_t;

/**
 * @brief   Top-level BCU runtime state
 */
typedef struct
{
    WheelData_t     wheel[WHEEL_COUNT];  /**< Per-wheel data array indexed by WheelId_t */
    float           vehicle_ref_speed;  /**< Reference vehicle speed (fastest non-slipping wheel) */
    float           brake_pressure_bar; /**< Brake master cylinder pressure from sensor */
    bool            abs_active;         /**< True if any wheel is in ABS cycle           */
    bool            ebd_active;         /**< True if EBD is redistributing brake force   */
    uint32_t        ignition_cycle;     /**< Persistent counter from NVM                 */
    uint32_t        dtc_count;          /**< Active DTC count                            */
} BCURuntime_t;


